package org.nercel.edu.extractexcel;


public class ExcelMergion {

	private int cellForm;
	private int cellTo;
	private int rowFrom; 
	private int rowTo;
	public int getCellForm() {
		return cellForm;
	}
	public void setCellForm(int cellForm) {
		this.cellForm = cellForm;
	}
	public int getCellTo() {
		return cellTo;
	}
	public void setCellTo(int cellTo) {
		this.cellTo = cellTo;
	}
	public int getRowFrom() {
		return rowFrom;
	}
	public void setRowFrom(int rowFrom) {
		this.rowFrom = rowFrom;
	}
	public int getRowTo() {
		return rowTo;
	}
	public void setRowTo(int rowTo) {
		this.rowTo = rowTo;
	}
}
